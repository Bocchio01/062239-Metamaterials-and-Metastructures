%% Piezo data

piezo = struct( ...
    'Y11_E', 62e9, ...
    'C_T', 7.0e-9, ...
    'k31', 0.351, ...
    'h', 1e-3, ...
    'b', 20e-3, ...
    'L', 22e-3, ...
    'rho', 7900);

piezo.A = piezo.b .* piezo.h;
piezo.J = piezo.b * (piezo.h^3) / 12 + piezo.A * (beam.h(1)/2 + piezo.h/2)^2;
piezo.C_S = piezo.C_T * (1 - piezo.k31^2);


