%% Beam data

% Odd elements corresponds to sandwiched beams section
beam = struct( ...
    'E', 69e9, ...
    'h', 1e-3, ...
    'b', 20e-3, ...
    'L', [690 576 1134] * 1e-3, ...
    'rho', 2700 ...
    );

beam.A(:) = beam.b(:) .* beam.h(:);
beam.J(:) = beam.b(:) .* (beam.h(:).^3) / 12;

% % Define a single cell as an array of 3 piezos
